import React from "react";

const DateInfo = () => {
  const date = new Date();
  const months = [
    "Jan",
    "Feb",
    "Mar",
    "Apr",
    "May",
    "Jun",
    "Jul",
    "Aug",
    "Sep",
    "Oct",
    "Nov",
    "Dec",
  ];
  const day = date.getDate();
  const month = months[new Date().getMonth()];
  const year = date.getFullYear() - 2000;
  return (
    <>
      {day}, {month}, {year}
    </>
  );
};

export default DateInfo;
